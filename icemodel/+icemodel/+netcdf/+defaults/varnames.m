function out = varnames(whichdata)

   % Define the grid and time dimension names
   dimvars = {
      'gridcell', ...
      'latitude', ...
      'longitude', ...
      'x_easting', ...
      'y_northing', ...
      'elevation', ...
      'depth', ...
      'time', ...
      };

   assert(strcmp(dimvars{end}, 'time'));

   % Define the variable names
   switch whichdata

      case 'ice1'

         out = {
            'Tsfc', ...
            'melt', ...
            'freeze', ...
            'subl', ...
            'cond', ...
            'runoff', ...
            'depth_melt', ...
            'depth_freeze', ...
            'surf_runoff', ...
            'column_runoff', ...
            };

      case 'ice2'

         out = {
            'Tice', ...
            ...'df_liq', ...
            ...'f_ice', ...
            ...'f_liq', ...
            };

      case 'met'

      case {'dimensions', 'dims'}
         out = dimvars;

      otherwise
         error('unrecognized icemodel data file name')
   end

   % Return as a column
   out = out(:);
end
