function dump_turbulent_heat_flux_debug_state(reason, Ts_old, Ts, tair, ...
      Qsi, Qli, albedo, wspd, ppt, tppt, psfc, De, ea_atm, chi, roL, br_coefs, ...
      liqflag, T, k_eff, dz, ro_sfc, snow_depth, opts)
   %DUMP_TURBULENT_HEAT_FLUX_DEBUG_STATE Save THF/SEB failure diagnostics.
   %
   % This helper writes a compact, replayable scalar debug record for the
   % turbulent-flux and surface-energy-balance stack. It is intended for
   % targeted failure analysis when opts.debug is enabled. The saved state is
   % scalar-heavy, test-friendly, and reuses the same THF diagnostic structs
   % returned interactively by icemodel.surface.diagnose_turbulent_heat_fluxes(...).

   debug_file = getenv('ICEMODEL_DEBUG_THF_FILE');
   if isempty(debug_file)
      return
   end

   persistent h
   if isempty(h)
      h = 1e-10;
   end

   debug_state = struct();
   debug_state.timestamp_utc = datetime('now', 'TimeZone', 'UTC');
   debug_state.reason = reason;
   debug_state.scheme = char(opts.turbulent_flux_scheme);
   debug_state.surface = build_surface_state(Ts_old, Ts, tair, Qsi, Qli, ...
      albedo, wspd, ppt, tppt, psfc, De, ea_atm, chi, roL, br_coefs, liqflag, ...
      T, k_eff, dz, ro_sfc, snow_depth);
   debug_state.thf_at_Ts_old = diagnose_turbulent_flux(Ts_old, tair, wspd, ...
      psfc, ea_atm, De, br_coefs, ro_sfc, snow_depth, roL, liqflag, opts);
   debug_state.thf_at_Ts = diagnose_turbulent_flux(Ts, tair, wspd, psfc, ...
      ea_atm, De, br_coefs, ro_sfc, snow_depth, roL, liqflag, opts);
   debug_state.seb = diagnose_seb(Ts_old, Ts, tair, Qsi, Qli, albedo, wspd, ...
      ppt, tppt, psfc, De, ea_atm, chi, roL, br_coefs, liqflag, T, k_eff, dz, ...
      ro_sfc, snow_depth, opts, h);

   save(debug_file, 'debug_state');
end

function surface = build_surface_state(Ts_old, Ts, tair, Qsi, Qli, albedo, ...
      wspd, ppt, tppt, psfc, De, ea_atm, chi, roL, br_coefs, liqflag, T, k_eff, ...
      dz, ro_sfc, snow_depth)
   %BUILD_SURFACE_STATE Assemble the exact SEB/THF replay inputs.

   surface = struct( ...
      'Ts_old', Ts_old, ...
      'Ts', Ts, ...
      'tair', tair, ...
      'Qsi', Qsi, ...
      'Qli', Qli, ...
      'albedo', albedo, ...
      'wspd', wspd, ...
      'ppt', ppt, ...
      'tppt', tppt, ...
      'psfc', psfc, ...
      'De', De, ...
      'ea_atm', ea_atm, ...
      'chi', chi, ...
      'roL', roL, ...
      'br_coefs', br_coefs, ...
      'liqflag', liqflag, ...
      'T', T, ...
      'k_eff', k_eff, ...
      'dz', dz, ...
      'ro_sfc', ro_sfc, ...
      'snow_depth', snow_depth);
end

function thf = diagnose_turbulent_flux(T_sfc, tair, wspd, psfc, ea_atm, De, ...
      br_coefs, ro_sfc, snow_depth, roL, liqflag, opts)
   %DIAGNOSE_TURBULENT_FLUX Recompute THF and return its diagnostic struct.

   [Qe, Qh, diag] = icemodel.surface.diagnose_turbulent_heat_fluxes(T_sfc, ...
      tair, wspd, psfc, ea_atm, De, br_coefs, ro_sfc, snow_depth, roL, ...
      liqflag, opts);
   thf = struct('Qe', Qe, 'Qh', Qh, 'diag', diag);
end

function seb = diagnose_seb(Ts_old, Ts, tair, Qsi, Qli, albedo, wspd, ppt, ...
      tppt, psfc, De, ea_atm, chi, roL, br_coefs, liqflag, T, k_eff, dz, ro_sfc, ...
      snow_depth, opts, h)
   %DIAGNOSE_SEB Evaluate the failing SEB residual and local solver metrics.

   residual = @(Ts_local) icemodel.surface.surface_energy_balance_residual( ...
      Ts_local, tair, Qsi, Qli, albedo, wspd, ppt, tppt, psfc, De, ea_atm, chi, ...
      roL, br_coefs, CONDUCT(k_eff, T, dz, Ts_local), liqflag, ro_sfc, ...
      snow_depth, opts);

   residual_old = residual(Ts_old);
   residual_current = residual(Ts);
   residual_ta = residual(tair);
   dfdT_old = imag(residual(Ts_old + 1i * h)) / h;
   dfdT_ta = imag(residual(tair + 1i * h)) / h;

   if isfinite(dfdT_old) && abs(dfdT_old) > 0
      newton_from_Ts_old = Ts_old - residual_old / dfdT_old;
   else
      newton_from_Ts_old = NaN;
   end

   if isfinite(dfdT_ta) && abs(dfdT_ta) > 0
      newton_from_Ta = tair - residual_ta / dfdT_ta;
   else
      newton_from_Ta = NaN;
   end

   [root_brent, exitflag_brent, ok_brent] = icemodel.numerics.search_zero( ...
      residual, Ts_old, tair - 50, tair + 50, tair, 1e-3);

   seb = struct( ...
      'Qc_old', CONDUCT(k_eff, T, dz, Ts_old), ...
      'Qc', CONDUCT(k_eff, T, dz, Ts), ...
      'residual_at_Ts_old', residual_old, ...
      'residual_at_Ts', residual_current, ...
      'residual_at_Ta', residual_ta, ...
      'dfdT_complex_step_at_Ts_old', dfdT_old, ...
      'dfdT_complex_step_at_Ta', dfdT_ta, ...
      'newton_candidate_from_Ts_old', newton_from_Ts_old, ...
      'newton_candidate_from_Ta', newton_from_Ta, ...
      'fsearchzero_root_from_Ts_old', root_brent, ...
      'fsearchzero_exitflag', exitflag_brent, ...
      'fsearchzero_ok', ok_brent);
end
